class circle {
  constructor(radius) {
    this.radius = radius;
  }
  
  get diameter() {
    this.radius = diameter * 2;
  }
  
  get circumference(circumference) {
    this.radius = circumference / (2 * Math.PI);
  }
  
  get area() {
    return Math.PI * (this.radius ** 2)
  }
  
  set diameter(diameter)
  
  
}